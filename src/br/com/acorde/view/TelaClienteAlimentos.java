    private void consultarAlimento(){
        String sql = "select proteina, gordura, caloria, qtdProteina, qtdGordura, qtdCaloria from Alimento where nome = ? ";
        try{
            pst = conexao.prepareStatement(sql);
            pst.setString(1, tfNomeAlimento.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                  //tfNomeAlimento.setText(rs.getString(1));
                  tfProteina.setText(rs.getString(1));
                  tfGordura.setText(rs.getString(2));
                  tfCaloria.setText(rs.getString(3));
                  tfQtdProteina.setText(rs.getString(4));
                  tfQtdGordura.setText(rs.getString(5));
                  tfQtdCaloria.setText(rs.getString(6));    
            }
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
             e.printStackTrace();
        }
    }
